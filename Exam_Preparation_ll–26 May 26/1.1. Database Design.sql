CREATE TABLE
	addresses(id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
		"name" VARCHAR(100) NOT NULL);
CREATE TABLE
	categories(id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
		"name" VARCHAR(10) NOT NULL);
CREATE TABLE
	clients(id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
		"full_name" VARCHAR(50) NOT NULL,
		"phone_number" VARCHAR(20) NOT NULL);
CREATE TABLE
	drivers(id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
		"first_name" VARCHAR(30) NOT NULL,
		"last_name" VARCHAR(30) NOT NULL, 
		"age" INT NOT NULL CHECK (age > 0),
		"rating" NUMERIC(2, 1) DEFAULT 5.5);
CREATE TABLE
	cars(id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
		 "make" VARCHAR(20) NOT NULL,
		 "model" VARCHAR(20),
		 "year" INT NOT NULL CHECK(year > 0) DEFAULT 1,
		 "mileage" INT CHECK(mileage > 0) DEFAULT 1,
		 "condition" CHAR(1) NOT NULL,
		 "category_id" INT NOT NULL REFERENCES categories(id) ON DELETE CASCADE ON UPDATE CASCADE
	)
;
CREATE TABLE
	courses(id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
		"from_address_id" INT NOT NULL REFERENCES addresses(id) ON DELETE CASCADE ON UPDATE CASCADE,
		"start" TIMESTAMP NOT NULL,
		"bill" NUMERIC (10, 2) CHECK (bill > 0) DEFAULT 10,
		"car_id" INT NOT NULL REFERENCES cars(id) ON DELETE CASCADE ON UPDATE CASCADE,
		"client_id" INT NOT NULL REFERENCES clients(id) ON DELETE CASCADE ON UPDATE CASCADE
	);
CREATE TABLE
	cars_drivers(
		"car_id" INT NOT NULL REFERENCES cars(id) ON DELETE CASCADE ON UPDATE CASCADE,
		"driver_id" INT NOT NULL REFERENCES drivers(id) ON DELETE CASCADE ON UPDATE CASCADE
	)
;